      SUBROUTINE MU_INDEX_TO_MOD(I_CRATE,I_MOD,MU_CRATE,MU_MOD)
C----------------------------------------------------------------------
C-
C-   Purpose and Methods : give the muon module number and crate number
C-                         given the crate and module index

C-
C-   Inputs  : I_CRATE = crate index (1=062,2=072,...)
C-             I_MOD   = module (MAX) index in crate (1-11)
C-   Outputs : MU_CRATE = muon crate number (e.g. 162)
C-             MU_MOD   = muon module number (e.g. 217)
C-   Controls: 
C-
C-   Created   8-APR-1992   Darien Wood
C-
C----------------------------------------------------------------------
      IMPLICIT NONE
      INTEGER MU_MOD,I_CRATE,I_MOD,MU_CRATE
      INTEGER MOD_ARRAY(11,18),ICRATE_ARRAY(18)
      DATA ICRATE_ARRAY/062,072,082,152,162,172,022,052,112,142,
     &                  092,102,122,132,002,012,032,042/
      DATA MOD_ARRAY/217,227,237,210,220,230,211,221,231,000,000,
     &               117,127,137,110,120,130,111,121,131,116,136,
     &               016,026,036,010,020,030,011,021,031,000,000,
     &               212,222,232,213,223,233,214,224,234,000,000,
     &               112,122,132,113,123,133,114,124,134,115,135,
     &               012,022,032,013,023,033,015,025,035,000,000,
     &               147,140,141,142,143,144,145,146,000,000,000,
     &               247,240,241,242,243,244,206,216,236,246,000,
     &               107,100,101,102,103,104,105,106,000,000,000,
     &               207,200,201,202,203,204,205,215,235,245,000,
     &               264,274,265,275,266,276,267,277,253,255,250,
     &               260,270,261,271,262,263,272,273,251,000,000,
     &               160,161,162,163,164,165,166,167,150,153,000,
     &               061,062,064,065,067,000,000,000,000,000,000,
     &               294,295,304,305,296,297,306,307,283,285,280,
     &               290,291,300,301,292,293,302,303,281,000,000,
     &               190,191,192,193,194,195,196,197,180,183,000,
     &               091,092,094,095,097,000,000,000,000,000,000/
C
      MU_CRATE = 0
      MU_MOD = 0
C
      IF(I_CRATE.GE.1 .AND. I_CRATE.LE.18) THEN   
        MU_CRATE = ICRATE_ARRAY(I_CRATE)
        IF(I_MOD.GE.1 .AND. I_MOD.LE.11) THEN
          MU_MOD = MOD_ARRAY(I_MOD,I_CRATE)
        ENDIF
      ENDIF  
C
C----------------------------------------------------------------------
  999 RETURN
      END
