C----------------------------------------------------------------------
C-
C-   Purpose and Methods :   Initialize arrays for the geometry of SAMUS
C-        stations, and the outputs from the SAMUS-WAMUS triplet finders
C-
C-   Inputs  : None
C-
C
C   The matrices PIX1 and PIX2 hide in them the geometry of the SAMUS
C     stations.  Their contents, PIX1(I,J) and PIX2(I,J), are the two
C     U-centroids which match to a specific centroid pair (Xi,Yj).
C     If (PIX1 .NE. -1) and (PIX2 .EQ. -1), this means that only one
C     U is needed for that (I,J) pixel.  If both PIX1 and PIX2 are -1,
C     the pixel is not a valid one (station's central hole).
C  Note: These matrices, PIX1 and PIX2, are used for triplet evaluation
C     in both SAM-WAM overlap  AND  pure SAMUS.
C
C  The matrix SWBITS define the SAM-WAM TRIPLET FINDER output bits. Its
C     contents, SWBITS(I,J,K), are the specific bits which must be set
C     for each good triplet found in pixel (Xi,Yj).  The third index, K,
C     represent the two different configurations to which each quadrant
C     can be mapped to, i.e.
C                             K = 1 for quadrants (1,3)
C                             K = 2 for quadrants (2,4)
C     The matrix SWBITS is used only in the SAM-WAM triplet finders, NOT
C     in the SAMUS triplet finders.
C
C-   Created  10-JUL-1992   Guilherme Lima
C      
C----------------------------------------------------------------------
