        IF(I.LT.ND_35) THEN
          IF(X.GE.X_35(I) .AND. X.LT.X_35(I+1))THEN
            PADSUM1=PADSUM_35(I)+(PADSUM_35(I+1)-PADSUM_35(I))
     &            *(X-X_35(I))/(X_35(I+1)-X_35(I))
            GOTO 51
          ELSE 
            GOTO 50
          ENDIF
        ELSEIF(I.GE.ND_35) THEN
          MSPADS=0.0
          GOTO 999
        ENDIF

   51   I=0
   52   I=I+1
        IF(I.LT.ND_45) THEN
          IF(X.GE.X_45(I) .AND. X.LT.X_45(I+1))THEN
            PADSUM2=PADSUM_45(I)+(PADSUM_45(I+1)-PADSUM_45(I))
     &            *(X-X_45(I))/(X_45(I+1)-X_45(I))
            GOTO 53
          ELSE 
            GOTO 52
          ENDIF
        ELSEIF(I.GE.ND_45) THEN
          MSPADS=0.0
          GOTO 999
        ENDIF

   53   PADSUM3=PADSUM1+(PADSUM2-PADSUM1)*(ANGLE-35.)/10.

      ELSEIF(ANGLE.GE.45.)THEN 
        I=0
   60   I=I+1
        IF(I.LT.ND_45) THEN
          IF(X.GE.X_45(I) .AND. X.LT.X_45(I+1))THEN
            PADSUM3=PADSUM_45(I)+(PADSUM_45(I+1)-PADSUM_45(I))
     &            *(X-X_45(I))/(X_45(I+1)-X_45(I))
            GOTO 61
          ELSE 
            GOTO 60
          ENDIF
        ELSEIF(I.GE.ND_45) THEN
          MSPADS=0.0
          GOTO 999
        ENDIF
   61   CONTINUE

      ENDIF

      IF(THETA.EQ.0..OR.THETA.EQ.180.) THEN
        MSPADS=999.0
      ELSE
        THETA=THETA*3.1415/180.
        MSPADS=PADSUM3/SIN(THETA)
      ENDIF

  999 RETURN
      END
