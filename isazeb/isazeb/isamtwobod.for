      SUBROUTINE ISAMTWOBOD(EMA,EM1,EM2,E1,E2,P1,P2)
CC  DOES TWO BODY DECAY IN C.M.
CC  A--> 1 + 2  EMA,EM1,EM2 ARE MASSES   E1 + E2 ARE ENERGIES
CC  P1 AND P2 ARE MOMENTUM
CC    DH 8/84
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
      REAL EMA,EM1,EM2,E1,E2,P1,P2
      E1=(EMA**2+EM1**2-EM2**2)/2./EMA
      E2=(EMA**2+EM2**2-EM1**2)/2./EMA
      P1=SQRT(E1**2-EM1**2)
      P2=SQRT(E2**2-EM2**2)
      RETURN
      END
