     +  1.00,  1.00,  1.00,  0.75,  0.76,  0.76,  0.74,  0.74,  0.72,
     +  1.00,  1.00,  1.00,  0.75,  0.86,  0.86,  0.68,  0.69,  0.71,
     +  1.00,  1.00,  1.00,  1.50,  1.44,  0.88,  0.76,  0.78,  0.72,
