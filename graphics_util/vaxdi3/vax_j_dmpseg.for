      SUBROUTINE J_DMPSEG(IUNITD)
      INCLUDE 'D0$INC:DI3INC.INC'
      WRITE(IUNITD,444)ISPTR,NSEGS
 444  FORMAT(' ISPTR,NSEGS:',2I)
      DO 448 KKK=1,NSEGS
      WRITE(IUNITD,447)KKK,NUMSEG(KKK),NCALLS(KKK),INDS1(KKK)
 447  FORMAT(' N,NUMSEG,NCALLS,INDS1:',4I)
  448 CONTINUE
      WRITE(IUNITD,445)(IBLOC(III),III=1,ISPTR)
 445  FORMAT(1000(2X,5(2X,O),/))
      END
