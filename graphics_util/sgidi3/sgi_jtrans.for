      SUBROUTINE JTRANS(MAT,IT,P1,P2,P3,P4,P5,P6,P7)
      REAL MAT(4,4)
      IF(IT.LT.1.OR.IT.GT.11) RETURN
C CLEAR FIRST
C
      DO I=1,4
        DO J=1,4
          MAT(I,J)=0.
        ENDDO
      ENDDO
      DO I=1,4
        MAT(I,I)=1.
      ENDDO
C
      CALL PUSHMA
      CALL LOADMA(MAT)
      GO TO (10,10,30,40,40,40,40,40,40,100,110) IT
   10 IF(IT.EQ.1) GO TO 50
   25 CALL TRANSL(P1,P2,P3)
      GO TO 50
   30 CALL SCALE(P4,P5,P6)
      GO TO 25
   40 ANG=P1
      IF(IT.LE.6) ANG=ANG*180./3.1415927
      GO TO (41,42,43,41,42,43) IT-3
   41 CALL ROT(ANG,'X')
      GO TO 50
   42 CALL ROT(ANG,'Y')
      GO TO 50
   43 CALL ROT(ANG,'Z')
      GO TO 50
  100 CONTINUE
      GO TO 50
  110 CONTINUE
   50 CONTINUE
      CALL GETMAT(MAT)
      CALL POPMAT
      RETURN
      END
